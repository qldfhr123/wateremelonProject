package main;

public class FlagDTO {
	private Boolean recentFlag;
	private Boolean popularFlag;
	private Boolean musiclistFlag;
	private Boolean myplaylistFlag;
	
	public Boolean getRecentFlag() {
		return recentFlag;
	}
	public void setRecentFlag(Boolean recentFlag) {
		this.recentFlag = recentFlag;
	}
	public Boolean getPopularFlag() {
		return popularFlag;
	}
	public void setPopularFlag(Boolean popularFlag) {
		this.popularFlag = popularFlag;
	}
	public Boolean getMusiclistFlag() {
		return musiclistFlag;
	}
	public void setMusiclistFlag(Boolean musiclistFlag) {
		this.musiclistFlag = musiclistFlag;
	}
	public Boolean getMyplaylistFlag() {
		return myplaylistFlag;
	}
	public void setMyplaylistFlag(Boolean myplaylistFlag) {
		this.myplaylistFlag = myplaylistFlag;
	}
	

	
}
